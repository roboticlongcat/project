def changeColor(image, color, BLEND_RGBA_MULT, BLEND_RGBA_ADD):
    image = image.copy()

    # zero out RGB values
    image.fill((0, 0, 0, 255), None, BLEND_RGBA_MULT)
    # add in new RGB values
    image.fill(color[0:3] + (0,), None, BLEND_RGBA_ADD)

    return image

def get_corder(field, number):
    coords = []
    for y in range(len(field)):
        for x in range(len(field[y])):
            if field[y][x] == number:
                coords.append([x, y])
    return coords
